from sklearn.neighbors import KNeighborsClassifier
from sklearn.discriminant_analysis import LinearDiscriminantAnalysis
from sklearn.naive_bayes import GaussianNB


def launch(train_, test_, model_, target_, name):
    m = [RidgeClassifier(random_state=42), KNeighborsClassifier(), LinearSVC(random_state=42),
         RandomForestClassifier(random_state=42, n_estimators=10), LogisticRegression(random_state=42),
         LinearDiscriminantAnalysis(), GaussianNB()]
    X, y = train_.drop(target_, axis=1).values, train_[target_].values
    X_train, y_train = X, y
    X_test, y_test = test_.drop(target_, axis=1).values, test_[target_].values
    m[model_].fit(X_train, y_train)
    y_pred = m[model_].predict(X_test)
    matrix = confusion_matrix(y_test, y_pred)
    score = round(recall_score(y_true=y_test, y_pred=y_pred, average="macro")*100, 2)
    print(name, score, matrix.tolist())
    return score


def comparison(name_train, name_test, target, drops, wo_selection_method, subset):
    train = read(filename=name_train)
    test = read(filename=name_test)
    target = target
    train, test = train.drop(drops, axis=1), test.drop(drops, axis=1)
    s1 = launch(train, test, wo_selection_method, target, "Without Selection  :")
    selected = (train.loc[:, [column for i, column in enumerate(train.columns[:-1]) if subset[i] == 1]]).columns
    train, test = train[list(selected) + [target]], test[list(selected) + [target]]
    s2 = launch(train, test, subset[-1], target, "Best Feature Subset:")
    print("\n")
    return s1, s2


